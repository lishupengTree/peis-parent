package com.lsp.his.exception;

public class MwInventoryException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -2312153731428078499L;

	public MwInventoryException() {
		super();
	}

	public MwInventoryException(String message, Throwable cause) {
		super(message, cause);
	}

	public MwInventoryException(String message) {
		super(message);
	}

	public MwInventoryException(Throwable cause) {
		super(cause);
	}
}
